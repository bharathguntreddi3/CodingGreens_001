def birthdayCakeCandles(candles):
    # Write your code here
    temp = 0
    gre = max(candles)    # find maximum of the given candles
    for i in candles:
        if i == gre:   
            temp += 1    # if the each iterator is equal to the gre then increment temp
    return temp 
    
